public class WrapperClassUsage {

    public static void main(String[] args) {
        // boolean
        Boolean isTrue = true;
        System.out.print("Boolean isTrue: ");
        System.out.println(isTrue);

        // byte
        Byte numberByte = 120;
        System.out.print("Byte numberByte: ");
        System.out.println(numberByte);

        // char
        Character character = 'a';
        System.out.print("Character character: ");
        System.out.println(character);

        // int
        Integer numberInt = 100000;
        System.out.print("Integer numberInt: ");
        System.out.println(numberInt);

        // float
        Float numberFloat = 35e3f;
        System.out.print("Float numberFloat: ");
        System.out.println(numberFloat);

        // double
        Double numberDouble = 12E4d;
        System.out.print("Double numberDouble: ");
        System.out.println(numberDouble);

        // long
        Long numberLong = 15000000000L;
        System.out.print("Long numberLong: ");
        System.out.println(numberLong);

        // short
        Short numberShort = 5000;
        System.out.print("Short numberShort: ");
        System.out.println(numberShort);
    }

}
