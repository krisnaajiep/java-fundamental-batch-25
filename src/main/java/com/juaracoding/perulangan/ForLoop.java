public class ForLoop {

    public static void main(String[] args) {

        // int i = 1 (control-variable-expression)
        // i <= 5 (continue-condition)
        // i++ (adjustment expression)
        // System.out.println(i) (loop-body)
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        // next statement

    }

}
